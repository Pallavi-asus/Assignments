#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Displays numbers recursively from 1 to 5
//  Input         : None
//  Output        : void
//  Time Complexity : O(N) 
//
////////////////////////////////////////////////////////////
void Display()
{
    static int iCnt = 1;  

    if (iCnt <= 5)  
    { 
        cout << iCnt << "\t";  
        iCnt++;              
        Display();             
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION FOR THE APPLICATION
//
////////////////////////////////////////////////////////////
int main()
{
    Display(); 
    return 0;
}


#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Displays characters recursively from 'A' to 'E'
//  Input         : None
//  Output        : void
//  Time Complexity : O(N) 
//
////////////////////////////////////////////////////////////
void Display()
{
    static char ch = 'A';  

    if (ch <= 'E')          
    {
        cout << ch << "\t"; 
        ch++;               
        Display();          
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION FOR THE APPLICATION
//
////////////////////////////////////////////////////////////
int main()
{
    Display(); 
    return 0;
}


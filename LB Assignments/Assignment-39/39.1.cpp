#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Displays '*' recursively 6 times
//  Input         : None
//  Output        : void
//  Time Complexity : O(N) 
//
////////////////////////////////////////////////////////////
void Display()
{
    static int iCnt = 0; 

    if (iCnt < 5) 
    { 
        cout << "*\t";  
        iCnt++;       
        Display();   
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION FOR THE APPLICATION
//
////////////////////////////////////////////////////////////
int main()
{
    Display(); 
    return 0;
}

